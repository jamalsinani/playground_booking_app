  Future<void> fetchStadiumData() async {
    try {
      // Fetch basic stadium data
      final data = await OwnerStadiumService.getStadiumById(widget.stadiumId);
      
      // Handle location data
      final lat = double.tryParse(data['latitude']?.toString() ?? '');
